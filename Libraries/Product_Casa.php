<?php

class Product_Casa extends Product {
	
	public $product_id = null;
	
	public function __construct ( $product_id = null ) {
		
		$this->product_id = $product_id;
		return true;
		
	}
	
	private function __Set_Product () {
		
		$this->product_id;
		
	}
	
	private function __Save_Product () {
		
		$this->product_id;
		
	}

}
